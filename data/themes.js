const themes = [];
let counter = 1;

outer: for (const archetype of archetypes) {
  for (const situation of situations) {
    for (const twist of twists) {
      for (const texture of textures) {
        const id = `theme-${String(counter).padStart(4, '0')}`;
        const text = `${archetype}を${situation} ${twist}${texture}`;
        themes.push({ id, text });
        counter += 1;
        if (counter > 1000) {
          break outer;
        }
      }
    }
  }
}

export { themes };
